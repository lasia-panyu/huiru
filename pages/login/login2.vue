<script>
	import {
		mapMutations,
		mapActions
	} from 'vuex'
	import config from '@/admin.config.js'
	import {
		getDeviceUUID
	} from '@/js_sdk/uni-admin/util.js'
	import {
		request
	} from '@/js_sdk/uni-admin/request.js'
	const captchaOptions = {
		deviceId: getDeviceUUID(),
		scene: 'login'
	}

	export default {
		data() {
			return {
				...config.navBar,
				indexPage: config.index.url,
				showPassword: true,
				loading: false,
				formData: {
					username: '',
					password: '',
					captcha: '',
				},
				isLoginPage:true,
				bgType: ['/hybrid/html/index1.html', '/hybrid/html/index2.html', '/hybrid/html/index3.html'],
				bg: '',
				captchaLoading: true,
				needCaptcha: true,
				captchaBase64: '',
				rules: {
					// 对name字段进行必填验证
					username: {
						rules: [{
								required: true,
								errorMessage: '请输入账户',
							},
							{
								minLength: 1,
								maxLength: 30,
								errorMessage: '账户长度在{minLength}到{maxLength}个字符',
							}
						]
					},
					// 对email字段进行必填验证
					password: {
						rules: [{
								required: true,
								errorMessage: '请输入正确的密码',
							},
							{
								minLength: 6,
								errorMessage: '密码长度大于{minLength}个字符',
							}
						]
					},
					// 对email字段进行必填验证
					captcha: {
						rules: [{
							required: true,
							errorMessage: '请输入验证码',
						}]
					},

				}
			}
		},
		onShow() {

		},
		async onLoad() {
			// let result= await this.$util.cloudFunction("data-transfer","logs",{"user_id":"001882"});
			this.createCaptcha();	
		},
		onReady() {

		},
		mounted() {
			// #ifdef H5
			this.focus()
			// #endif
			const self = this
			// uni.getStorage({
			// 	key: "lastUsername",
			// 	success: function(res) {
			// 		self.formData.username = res.data
			// 	}
			// })
			// this.getNeedCaptcha()
		},
		methods: {
			...mapActions({
				init: 'app/init'
			}),
			...mapMutations({
				setToken(commit, tokenInfo) {
					commit('user/SET_TOKEN', tokenInfo)
				}
			}),
			submit(event) {
				if (this.loading) {
					return
				}
				const {
					errors,
					value
				} = event.detail
				if (errors) {
					return
				}
				// #ifdef H5
				this.$refs.usernameInput.$refs.input.blur()
				this.$refs.passwordInput.$refs.input.blur()
				this.$refs.captchaInput && this.$refs.captchaInput.$refs.input.blur()
				// #endif
				this.loading = true;
				console.log(value)
				request('login', {
					...value,
					captchaOptions
				}, {
					functionName: 'uni-id-cf',
					showModal: false
				}).then(res => {
					console.log(res);
					uni.setStorageSync('uni_id_token', res.token);
					uni.setStorageSync('uni_id_token_expired', res.tokenExpired);
					this.$u.route({
						url: 'pages/news/news',
						type: 'switchTab',
					});
					// this.setToken({
					//  token: res.token,
					//  tokenExpired: res.tokenExpired
					// })
					// return this.init().then(() => {
					//  uni.showToast({
					//      title: '登录成功',
					//      icon: 'none'
					//  })
					//  uni.setStorage({
					//      key: 'lastUsername',
					//      data: value.username
					//  });
					//  uni.redirectTo({
					//      url: "/pages/news/news",
					//  })
					// })
				}).catch(err => {
					console.log(4444, err);
					if (err.needCaptcha) {
						this.formData.captcha = ''
						this.createCaptcha()
						this.needCaptcha = true
					}
					const that = this
					uni.showModal({
						content: err.message || '请求服务失败',
						showCancel: false,
						success: function() {
							if (err.code === 10101 && that.$refs.usernameInput) {
								that.$refs.usernameInput.$refs.input.focus()
							}
							if (err.code === 10102 && that.$refs.passwordInput) {
								that.$refs.passwordInput.$refs.input.focus()
							}
							if (err.code === 10002 && that.$refs.captchaInput) {
								that.$refs.captchaInput.$refs.input.focus()
							}
						}
					})
				}).finally(err => {
					this.loading = false
				})

			},

			createCaptcha() {
				console.log("createCaptcha");
				this.captchaLoading = true;
				// console.log(request);
				request('createCaptcha', captchaOptions, {
					functionName: 'uni-id-cf'
				}).then(res => {
					if (res.code === 0) {
						this.captchaBase64 = res.captchaBase64
					}
				}).catch(err => {}).finally(err => {
					this.captchaLoading = false
				})
			},

			confirmForm(name, value) {
				// this.binddata(name, value)
				this.submitForm()
			},
			submitForm() {
				this.$refs.form.submit()
			},
			initAdmin() {
				uni.redirectTo({
					url: '/pages/demo/init/init'
				})
			},
			changePassword: function() {
				this.showPassword = !this.showPassword;
			},
			// #ifdef H5
			focus: function() {
				this.$refs.usernameInput.$refs.input.focus()
			},
			// #endif
		}
	}
</script>

